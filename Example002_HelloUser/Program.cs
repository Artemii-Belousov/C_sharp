// Чтобы вывести данные в терминал с переводом на новую строку, необходимо ввести команду Console.WriteLine()
// Чтобы вывести данные в терминал без перевода на новую строку, необходимо ввести команду Console.Write()
Console.Write("Enter your name");
// Чтобы считать данные, необходимо ввести команду Console.ReadLine()
string username = Console.ReadLine();
Console.Write("Hello, ");
Console.Write(username);